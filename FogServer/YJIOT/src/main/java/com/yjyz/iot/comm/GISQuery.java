package com.yjyz.iot.comm;

public class GISQuery {
	private String appId;
	private Double longitudeLB;
	private Double longitudeRT;
	private Double latitudeLB;
	private Double latitudeRT;
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public Double getLongitudeLB() {
		return longitudeLB;
	}
	public void setLongitudeLB(Double longitudeLB) {
		this.longitudeLB = longitudeLB;
	}
	public Double getLongitudeRT() {
		return longitudeRT;
	}
	public void setLongitudeRT(Double longitudeRT) {
		this.longitudeRT = longitudeRT;
	}
	public Double getLatitudeLB() {
		return latitudeLB;
	}
	public void setLatitudeLB(Double latitudeLB) {
		this.latitudeLB = latitudeLB;
	}
	public Double getLatitudeRT() {
		return latitudeRT;
	}
	public void setLatitudeRT(Double latitudeRT) {
		this.latitudeRT = latitudeRT;
	}

	
}
